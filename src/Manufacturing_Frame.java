   private void loadTradeNames(int lineId) {
        Connection conn = null;
        PreparedStatement pstmt = null;
        ResultSet rs = null;

        try {
            conn = DatabaseUtil.getConnection();
            String sql = "SELECT TradeName FROM Medicines WHERE LineID = ?";
            pstmt = conn.prepareStatement(sql);
            pstmt.setInt(1, lineId);
            rs = pstmt.executeQuery();

            List<String> tradeNames = new ArrayList<>();
            while (rs.next()) {
                String tradeName = rs.getString("TradeName");
                System.out.println("Retrieved TradeName: " + tradeName); // Debugging output
                tradeNames.add(tradeName);
            }

            if (tradeNames.isEmpty()) {
                System.out.println("No trade names found for LineID: " + this.lineId); // Debugging output
            }

            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(tradeNames.toArray(new String[0]));
            cmb_Trade_Name.setModel(model);

        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading trade names from database.");
        } finally {
            DatabaseUtil.close(conn, pstmt, rs);
        }
    }
   
