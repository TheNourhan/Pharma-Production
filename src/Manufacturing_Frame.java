    private void btn_Manufacturing_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_Manufacturing_ActionPerformed
        Connection conn = null;
        PreparedStatement pstmtInsert = null;
        PreparedStatement pstmtUpdate = null;
        ResultSet rs = null;

        try {
            // Validate Expiration Date
            java.util.Date selectedDate = date_Expir.getDate();
            if (selectedDate != null) {
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                String formattedDate = dateFormat.format(selectedDate);
                System.out.println("Selected Expiration Date: " + formattedDate);
            } else {
                System.out.println("No date selected");
            }
            /*
            java.sql.Date currentDate = new java.sql.Date(System.currentTimeMillis());
            if (selectedDate.before(currentDate)) {
                JOptionPane.showMessageDialog(this, "Expiration Date cannot be earlier than the current date.");
                return;
            }*/

            // Retrieve selected TradeName and Quantity
            String tradeName = (String) cmb_Trade_Name.getSelectedItem();
            int quantity = Integer.parseInt(txt_Quantity.getText());

            conn = DatabaseUtil.getConnection();
            conn.setAutoCommit(false); // Start transaction

            // Get MedicineID for the selected TradeName
            String sqlSelect = "SELECT MedicineID FROM Medicines WHERE TradeName = ? AND LineID = ?";
            pstmtInsert = conn.prepareStatement(sqlSelect);
            pstmtInsert.setString(1, tradeName);
            pstmtInsert.setInt(2, this.lineId);
            rs = pstmtInsert.executeQuery();

            if (rs.next()) {
                int medicineId = rs.getInt("MedicineID");

                // Insert into ManufacturingDetails
                String sqlInsert = "INSERT INTO ManufacturingDetails (MedicineID, ManufacturingDate, ExpirationDate, Quantity) VALUES (?, ?, ?, ?)";
                pstmtInsert = conn.prepareStatement(sqlInsert);
                pstmtInsert.setInt(1, medicineId);
                pstmtInsert.setDate(2, new java.sql.Date(System.currentTimeMillis())); // Use current date for ManufacturingDate
                pstmtInsert.setDate(3, new java.sql.Date(selectedDate.getTime()));
                pstmtInsert.setInt(4, quantity);
                pstmtInsert.executeUpdate();

                // Update TotalQuantity in Medicines
                String sqlUpdate = "UPDATE Medicines SET TotalQuantity = TotalQuantity + ? WHERE MedicineID = ?";
                pstmtUpdate = conn.prepareStatement(sqlUpdate);
                pstmtUpdate.setInt(1, quantity);
                pstmtUpdate.setInt(2, medicineId);
                pstmtUpdate.executeUpdate();

                conn.commit(); // Commit transaction
                JOptionPane.showMessageDialog(this, "Manufacturing details added successfully!");
            } else {
                JOptionPane.showMessageDialog(this, "Selected trade name not found for the given line ID.");
            }
        } catch (Exception ex) {
            ex.printStackTrace();
            try {
                if (conn != null) conn.rollback(); // Rollback transaction in case of error
            } catch (SQLException e) {
                e.printStackTrace();
            }
            JOptionPane.showMessageDialog(this, "Error during manufacturing process.");
        } finally {
            DatabaseUtil.close(conn, pstmtInsert, rs);
            DatabaseUtil.close(conn, pstmtUpdate);
        }
    }//GEN-LAST:event_btn_Manufacturing_ActionPerformed

